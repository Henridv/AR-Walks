package com.vop.tools.matrix;

public class Dimension {
	private int cols, rows;
	
	public Dimension(int cols, int rows) {
		this.cols = cols;
		this. rows = rows;
	}

	public int getCols() {
		return cols;
	}

	public void setCols(int cols) {
		this.cols = cols;
	}

	public int getRows() {
		return rows;
	}

	public void setRows(int rows) {
		this.rows = rows;
	}

}
